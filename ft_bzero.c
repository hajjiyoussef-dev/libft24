#include "libft.h"

void	ft_bzero(void *s,	size_t num)
{
	unsigned char	*p;
	size_t			i;

	i = 0;
	p = (unsigned char *)s;
	while (i < num)
	{
		p[i] = 0;
		i++;
	}
}
