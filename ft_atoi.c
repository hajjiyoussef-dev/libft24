#include "libft.h"

int	ft_atoi(const char *c)
{
	int	res;
	int	i;
	int	s;

	i = 0;
	res = 0;
	s = 1;
	while (c[i] == 32 || (c[i] >= 9 && c[i] <= 13))
	{
		i++;
	}
	if ((c[i] == '+' || c[i] == '-'))
	{
		if (c[i] == '-')
			s *= -1;
		i++;
	}
	while (c[i] != '\0' && (c[i] >= '0' && c[i] <= '9'))
	{
		res = res * 10 + (c[i] - '0');
		i++;
	}
	return ((res * s));
}
